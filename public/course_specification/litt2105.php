          <td>Course Description</td>
          <td class="text-center">:</td>
          <td colspan="2" class="text-justify">
            This introductory course provides a venue to develop
            student creativity and fluency in different genres as
            well as critical reading of texts through writing
            assignments and workshops, acquainting the students
            with the literary works of representative authors from
            the nation, the region and the world.
          </td>
        </tr>
        <tr>
          <td>Course Objectives</td>
          <td class="text-center">:</td>
          <td colspan="2" class="text-justify">
            Write poetry, prose, drama and other hybrid forms.
            Expose the students to important literary modelsin
            major genres and their social relevance.
            Criticize or review a literary piece in terms of textual
            and contextual considerations.
            Write a reflection paper on assigned texts applying
            basic concepts and methods.
            Participate in a workshop on a poetry, prose and
            drama.
          </td>
