        <tr>
          <td>Course Description</td>
          <td class="text-center">:</td>
          <td colspan="2" class="text-justify">
            The course introduces the students to the range of
            theoretical and practical issues in academic and
            professional settings involving written, oral and other
            communication skills, and documentation and research
            principles and protocols intersecting disciplines like
            literary and cultural studies, English studies, creative
            writing, media studies, education, and communication,
            and professions in publishing, advertising, broadcasting,
            journalism, law, marketing, customer service, among
            others. It also provides hands-on work experience in
            literature-specific academic settings and Literature-generic careers through the Practicum requirement in
            order to meet both disciplinal and professional
            outcomes.
          </td>
        </tr>
        <tr>
          <td>Learning/Course Outcomes</td>
          <td class="text-center">:</td>
          <td colspan="2">
            a) Apply close reading of texts in the analysis and
            interpretation of texts across the professions,
            such as advertising, marketing, media, etc.
            <br>
            b) Deploy critical perspectives in the analysis and
            interpretation of texts across the professions.
            <br>
            c) Produce a literary or non-literary text in one of
            the genres, forms or types across the
            professions.
            <br>
            d) Produce a non-literary text that may be useful
            across the professions.
            <br>
            e) With the above-mentioned skills and
            competencies, plan and execute written and
            non-written work or projects for careers in
            which Literature graduates are traditionally
            considered highly competitive including Media,
            Advertising, Marketing, Administration, Civil
            Service, Customer Relations, BPOs, and others.
          </td>
        </tr>
