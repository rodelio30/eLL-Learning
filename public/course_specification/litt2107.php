        <tr>
          <td>Course Description</td>
          <td class="text-center">:</td>
          <td colspan="2" class="text-justify">
            The course introduces the students to various research
            methods in literary and cultural studies, and across the
            professions. Focus is on skills in analyzing, interpreting
            materials, and implementing data/text gathering
            protocols and procedures and the theoretical and
            conceptual frameworks that influence the methods.
            Students are expected to submit a sound and original
            research proposal and a draft paper in preparation for
            oral defense.
          </td>
        </tr>
        <tr>
          <td>Course Objectives</td>
          <td class="text-center">:</td>
          <td colspan="2" class="text-justify">
            Students should be able to:
            <ol>
              <li>
                Submit an original and relevant research proposal in
                any topic in Philippine literature, including
                ruminations on the research methods being
                appropriated.
              </li>
              <li>
                Submit a reflective essay discussing the researcher’s
                own methods being appropriated.
              </li>
              Submit an original and relevant research on any
              topic in Philippine literature, including ruminations
              on the research methods being appropriated.
              </li>
              <li>
                Submit a reflexive essay discussing the researcher’s
                own methods, biases, limitations, etc.
              </li>
            </ol>
          </td>
        </tr>
