          <td>Course Outcomes</td>
          <td class="text-center">:</td>
          <td colspan="2">
            The students should be able to:
            <br>
            A. Write an interdisciplinary essay that draws on a
            range of critical concepts in cultural theory;
            <br>
            B. Discern the connection between and among the
            disciplines, for example, in an interdisciplinary
            essay.
            <br>
            C. Practice interdisciplinary thinking in an applied
            study or project.
