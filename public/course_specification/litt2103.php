          <td>Course Outline</td>
          <td class="text-center">:</td>
          <td colspan="2">
            A. Key theoretical movements in the study of
            cultural texts from material culture to
            neuroscience;
            B. Recent interdisciplinary methodologies in animal
            studies, digital humanities, visual culture,
            popular culture, affect studies, race and
            ethnicity, the anthropocene, and public
            humanities;
            C. Standard approaches to cultural analysis from
            close reading, to thick description, to visual
            analysis, to ethnography, to big data.
          </td>
