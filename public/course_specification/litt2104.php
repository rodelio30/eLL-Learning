        <tr>
          <td>Course Description</td>
          <td class="text-center">:</td>
          <td colspan="2" class="text-justify">
            A study of the foundational literary texts from the
            decolonizing world, the class introduces the student to
            the politics, motifs, and aesthetics of postcolonial
            writing. It foregrounds postcolonial texts from the
            Philkippines, as well as those from the Filipino diaspora,
            and explores them in relation to other postcolonial
            traditions in Asia, Africa, the Caribbean, and Latin
            America. Approaching literary studies from a
            postcolonial perspective, the class encourages the
            appreciation of a situated yet comparative knowledge.
          </td>
        </tr>
