        <tr>
          <td>Course Description</td>
          <td class="text-center">:</td>
          <td colspan="2" class="text-justify">
            An introduction to the art and practice of translation, as
            well as the history of translation theory. The course is
            divided into three parts, hoping to provide an analysis
            of the process as well as practical advice for intending
            translators, namely: the theory of translation, the
            practice of translation, and the appreciation of literary
            translations as texts. In covering these issues, the
            course stresses the importance of understanding the
            unfamiliar and the need to see human experience from
            as many angles.
          </td>
        </tr>
        <tr>
          <td>Course Outcomes</td>
          <td class="text-center">:</td>
          <td colspan="2">
            The students should be able to:
            A. Translate a literary text in a chosen genre with an
            accompanying critical introduction that reflects on
            the process of translation;
            B. Deploy the critical apparatus in translation theory;
            C. Discern the issues and challenges in the practice of
            translation.
            D. Know the representative texts in translation.
            E. Understand the history of translation theory.
          </td>
