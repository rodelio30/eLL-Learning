          <td colspan="2">
            <b>
              Introduction to the Postcolonial Tradition/
              Introduksyon sa Tradisyong Postkolonyal
            </b>
            <br> <br>
            <p class="text-justify">
              A study of the foundational literary texts from the
              decolonizing world, the course introduces the
              student to the politics, motifs and aesthetics of
              postcolonial writing. It foregrounds postcolonial
              texts from the Philippines, as well as those from
              the Filipino diaspora, and explores them in
              relation to other postcolonial traditions in Asia,
              Africa, the Caribbean, and Latin America.
              Approaching literary studies from a postcolonial
              perspective, the course encourages the
              appreciation of a situated yet comparative
              knowledge.
            </p>
