              This course introduces the students to various
              research methods in literary and cultural studies,
              and across the professions. Focus is on skills in
              analyzing, interpreting materials, and
              implementing data/text gathering protocols and
              procedures and the theoretical and conceptual
              frameworks that influence the methods. Students
              are expected to submit sound and original
              research proposal and a draft paper in
              preparation for oral defense.
