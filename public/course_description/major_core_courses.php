            <b>
              Introduction to Translation/
              Introduksyon sa Pagsasalin
            </b>
            <br> <br>
            <p class="text-justify">
              An introduction to the art and practice of
              translation, as well as the history of translation
              theory. The course is divided into three parts,
              hoping to provide an analysis of the process as
              well as practical advice for intending translators,
              namely: the theory of translation, the practice of
              translation, and the appreciation of literary
              translations as texts. In covering these issues, the
              course stresses the importance of understanding
              the unfamiliar and the need to see human
              experience from many angles.
            </p>
