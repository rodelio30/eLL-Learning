            <b>
              Introduction to Cultural Theory/
              Introduksyon sa Teoryang Kultural
            </b>
            <br> <br>
            <p class="text-justify">
              A survey of theories and methodologies in the
              interdisciplinary study of culture, the course
              examines how the cultural practices are codified,
              disseminated, interpreted, and appropriated as
              texts across a range of human endeavors. It
              explores questions that relate to the production of
              culture, the social implications of theory, the use
              of critique, and the aesthetics of form. Organized
              around the most influential theoretical paradigms
              in cultural theory today, it encourages the student
              to appreciate the relevance of culture in forming
              human societies and in securing the survival of
              the planet.
            </p>
