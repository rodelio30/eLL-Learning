            </b>
            <br> <br>
            <p class="text-justify">
              The course introduces the student to the range of
              theoretical and practical issues in academic and
              professional settings involving written, oral and
              other communication skills, and documentation
              and research principles and protocols intersecting
              disciplines like literary and cultural studies,
              English studies, creative writing, media studies,
              education and communication, and professions in
              publishing, advertising, broadcasting, journalism,
              law, marketing, customer service, among others.
              It also provides hands-on work experience in
              Literature-specific academic settings and
              Literature-generic careers through practicum
              requirement in order to meet both disciplinal and
              professional outcomes.
            </p>
