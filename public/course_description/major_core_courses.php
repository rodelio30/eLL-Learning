          <td colspan="2">
            <b>
              Introduction to Literary Theory/
              Introduksyon sa Teoryang Pampanitikan
            </b>
            <br> <br>
            <p class="text-justify">
              The course introduces the students to the
              scholarly discipline of literary studies to the basic
              tools, techniques and frameworks, a survey of
              methods and key concepts and categories in
              literary studies, the course examines how texts
              (“literary” versus “non-literary”) are produced and
              read. It explores fundamental questions that
              relate to the nature of literature, the function of
              interpretation, and the uses of a broad range of
              forms of textual communications today.
              Organized around the most influential theoretical
              paradigms in literary studies, it encourages the
              students to appreciate the relevance of
              description and evaluation of literary works
              toward developing in them analytical and
              interpretive skills needed to engage with and
              produce close readings of texts. The intellectual
              and practical skills learned from this course may
              extend into digital literatures and new media,
              critical methodologies used in conjunction with
              current technologies.
            </p>
